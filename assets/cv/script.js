// Function to apply language
function applyLanguage(lang) {
    const langConfig = translations[lang] || translations['en'];
    
    bodyElement.dir = langConfig.dir || 'ltr';
    bodyElement.lang = lang;
    
    const linksContainer = document.getElementById('nav-links-container');
    if (linksContainer) {
        linksContainer.dir = langConfig.dir || 'ltr';
    }
    
    if (lang === 'ar') {
        bodyElement.style.fontFamily = "'IBM Plex Sans Arabic', sans-serif";
    } else {
        bodyElement.style.fontFamily = "'Inter', sans-serif";
    }
    
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (langConfig[key]) {
            element.textContent = langConfig[key];
        }
    });
    
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (langConfig[key]) {
            element.placeholder = langConfig[key];
        }
    });
    
    langButtons.forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    
    const copyrightElement = document.getElementById('copyright');
    if (copyrightElement && langConfig.copyright) {
        copyrightElement.textContent = langConfig.copyright;
    }
    
    localStorage.setItem('selectedLang', lang);
    currentLang = lang;
}

// Setup event listeners
function setupEventListeners() {
    // Language buttons
    langButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            if (lang !== currentLang) {
                applyLanguage(lang);
            }
        });
    });
    
    // Smooth scrolling for all anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                // Calculate offset for fixed header
                const offset = 80;
                
                window.scrollTo({
                    top: targetElement.offsetTop - offset,
                    behavior: 'smooth'
                });
                
                // Update active nav link
                updateActiveNavLink(targetId.substring(1));
                
                // Close mobile menu if open
                if (mobileMenu && !mobileMenu.classList.contains('hidden')) {
                    mobileMenu.classList.add('hidden');
                    const icon = mobileMenuBtn.querySelector('i');
                    icon.classList.remove('fa-times');
                    icon.classList.add('fa-bars');
                }
            }
        });
    });

    // Modal Close Listeners
    const modal = document.getElementById('projectModal');
    if (modal) {
        // Close modal when clicking outside the content box
        modal.addEventListener('click', function(e) {
            if (e.target === this) {
                closeProjectModal();
            }
        });
    }

    // Close modal on Escape key press
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && modal && !modal.classList.contains('hidden')) {
            closeProjectModal();
        }
    });
}

// Setup scroll spy
function setupScrollSpy() {
    const observerOptions = {
        root: null,
        rootMargin: '-20% 0px -70% 0px',
        threshold: 0
    };
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const sectionId = entry.target.getAttribute('data-section');
                updateActiveNavLink(sectionId);
            }
        });
    }, observerOptions);
    
    sections.forEach(section => {
        observer.observe(section);
    });
}

// Update active navigation link
function updateActiveNavLink(sectionId) {
    navLinks.forEach(link => {
        if (link.getAttribute('href') === `#${sectionId}`) {
            link.classList.add('active');
        } else {
            link.classList.remove('active');
        }
    });
    
    currentSection = sectionId;
}

// Initialize mobile menu
function initMobileMenu() {
    if (!mobileMenuBtn || !mobileMenu) return;
    
    mobileMenuBtn.addEventListener('click', () => {
        mobileMenu.classList.toggle('hidden');
        const icon = mobileMenuBtn.querySelector('i');
        if (mobileMenu.classList.contains('hidden')) {
            icon.classList.remove('fa-times');
            icon.classList.add('fa-bars');
        } else {
            icon.classList.remove('fa-bars');
            icon.classList.add('fa-times');
        }
    });
    
    // Close menu when clicking outside
    document.addEventListener('click', (e) => {
        if (!mobileMenu.contains(e.target) && !mobileMenuBtn.contains(e.target)) {
            mobileMenu.classList.add('hidden');
            const icon = mobileMenuBtn.querySelector('i');
            icon.classList.remove('fa-times');
            icon.classList.add('fa-bars');
        }
    });
}
// Setup contact form with EmailJS
function setupForm() {
    if (!contactForm) return;
    
    contactForm.addEventListener('submit', function(e) {
        e.preventDefault();
        
        const nameVal = document.getElementById('name').value;
        const emailVal = document.getElementById('email').value;
        const subjectVal = document.getElementById('subject').value;
        const messageVal = document.getElementById('message').value;
        
        if (!nameVal || !emailVal || !messageVal) {
            alert(currentLang === 'ar' ? 'يرجى ملء جميع الحقول المطلوبة.' : 
                  currentLang === 'tr' ? 'Lütfen tüm gerekli alanları doldurun.' :
                  'Please fill in all required fields.');
            return;
        }
        
    
        const submitBtn = contactForm.querySelector('button[type="submit"]');
        const originalBtnText = submitBtn.innerHTML;
        submitBtn.innerHTML = currentLang === 'ar' ? 'جاري الإرسال <i class="fas fa-spinner fa-spin ml-2"></i>' : 'Sending...';
        submitBtn.disabled = true;
        
        const templateParams = {
            name: nameVal,
            email: emailVal,
            subject: subjectVal,
            message: messageVal
        };
        
        emailjs.send('service_aexwb6t', 'template_qpqiteg', templateParams)
            .then(function(response) {
                console.log('SUCCESS!', response.status, response.text);
                
                
                alert(currentLang === 'ar' ? 'شكراً لك على رسالتك! سأعود إليك قريباً.' :
                      currentLang === 'tr' ? 'Mesajınız için teşekkürler! Size en kısa sürede döneceğim.' :
                      'Thank you for your message! I will get back to you soon.');
                
                contactForm.reset();
                submitBtn.innerHTML = originalBtnText;
                submitBtn.disabled = false;
                
            }, function(error) {
                console.log('FAILED...', error);
                
                
                alert(currentLang === 'ar' ? 'حدث خطأ أثناء إرسال الرسالة. يرجى التأكد من اتصالك والمحاولة لاحقاً.' : 'Failed to send the message. Please try again later.');
                
                
                submitBtn.innerHTML = originalBtnText;
                submitBtn.disabled = false;
            });
    });
}
// Setup scroll animations
function setupScrollAnimations() {
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
    };
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, observerOptions);
    
    sections.forEach(section => {
        observer.observe(section);
    });
}

